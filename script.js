// Function to generate a random task for a specific stat
function generateRandomTask(stat) {
  const taskLevelElement = document.getElementById(`${stat}-tasks`);
  const taskDescriptionElement = document.getElementById(`${stat}-task`);

  const currentLevel = parseInt(taskLevelElement.textContent.replace('Tasks: ', ''));
  const nextLevel = currentLevel + 1;

  const taskLevel = Math.min(nextLevel, Object.keys(taskDifficulties[stat]).length);
  const tasks = Object.values(taskDifficulties[stat]);
  const randomTask = tasks[Math.floor(Math.random() * tasks.length)];

  taskLevelElement.textContent = `Tasks: ${nextLevel}`;
  taskDescriptionElement.textContent = `Task: ${randomTask}`;
}

// Function to save progress to local storage
function saveProgress(stat) {
  const taskLevelElement = document.getElementById(`${stat}-tasks`);
  const taskDescriptionElement = document.getElementById(`${stat}-task`);

  const currentLevel = parseInt(taskLevelElement.textContent.replace('Tasks: ', ''));
  const currentTask = taskDescriptionElement.textContent.replace('Task: ', '');

  localStorage.setItem(`${stat}-level`, currentLevel);
  localStorage.setItem(`${stat}-task`, currentTask);
}

// Retrieve progress for each stat when the page loads
function retrieveProgress(stat) {
  const taskLevelElement = document.getElementById(`${stat}-tasks`);
  const taskDescriptionElement = document.getElementById(`${stat}-task`);

  const storedLevel = localStorage.getItem(`${stat}-level`);
  const storedTask = localStorage.getItem(`${stat}-task`);

  if (storedLevel && storedTask) {
    taskLevelElement.textContent = `Tasks: ${storedLevel}`;
    taskDescriptionElement.textContent = `Task: ${storedTask}`;
  } else {
    taskLevelElement.textContent = 'Tasks: 1';
    taskDescriptionElement.textContent = `Task: ${taskDifficulties[stat][1]}`;
  }
}

// Generate initial tasks for each stat
for (const stat in taskDifficulties) {
  generateRandomTask(stat);
}

// Event listeners for generate and previous task buttons
document.querySelectorAll('button').forEach((button) => {
  button.addEventListener('click', () => {
    const stat = button.getAttribute('onclick').match(/\('([^']+)'\)/)[1];
    saveProgress(stat);
    generateRandomTask(stat);
  });
});

// Retrieve progress for each stat when the page loads
for (const stat in taskDifficulties) {
  retrieveProgress(stat);
}
